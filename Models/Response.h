#pragma once

// Перечисление возможных ответов/действий игрока в игре
enum class Response
{
    OK,      // Успешное выполнение хода - игрок сделал корректный ход
    BACK,    // Запрос на откат хода назад - игрок хочет отменить предыдущий ход
    REPLAY,  // Запрос на повтор игры - игрок хочет начать новую игру с начала
    QUIT,    // Выход из игры - игрок хочет завершить игру и закрыть приложение
    CELL     // Выбор клетки на доске - игрок кликнул на определенную клетку доски
};
